<script>
import { mapGetters } from 'vuex'
export default {
  name: 'home',
  data () {
    return {
      percent: 0,
      background: null,
      gradient: null,
      timer: null
    }
  },
  computed: {
    ...mapGetters({
      colors: 'getColors'
    })
  },
  created () {
    this.background = this.colors.background
    this.gradient = `linear-gradient(45deg, transparent 100%, ${this.colors.background} 0%`
  },
  watch: {
    'colors.background': {
      handler: function () {
        this.background = this.colors.background
        this.percent = 0
        let wait = null
        clearTimeout(wait)
        wait = setTimeout(() => {
          clearInterval(this.timer)
          this.fadeBackground()
        }, 50)
      },
      deep: true
    }
  },
  methods: {
    fadeBackground () {
      this.percent = 0
      let duration = 0
      let lastFrame = +new Date()
      this.timer = setInterval(() => {
        let now = +new Date()
        let deltaT = now - lastFrame
        this.percent = Math.ceil(duration += 5 * deltaT / 50)
        lastFrame = now
        this.gradient = `linear-gradient(45deg, transparent ${this.percent}%, ${this.colors.backgroundOld}) ${100 - this.percent}%`

        if (this.percent > 99) {
          this.colors.backgroundOld = this.colors.background
          this.percent = 0
          clearInterval(this.timer)
        }
      }, 25)
    }
  }
}
</script>
